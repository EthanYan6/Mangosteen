def gb_esc(s: str) -> str:
    return "".join(f"\\x{b:02X}" for b in s.encode("gb2312"))


def c_str(s: str) -> str:
    if "\n" in s:
        parts = s.split("\n")
        return '"' + "\\n".join(gb_esc(p) if any(ord(c) >= 128 for c in p) else p for p in parts) + '"'
    if all(ord(c) < 128 for c in s):
        return '"' + s.replace("\\", "\\\\").replace('"', '\\"') + '"'
    return '"' + gb_esc(s) + '"'


